package me.ipincamp;

public class IncrementDecrement {
    public static void main(String [] args) {
        /**
         * Increase
         */
        int number = 50;

        number++;
        System.out.println(number);
        /**
         * Decrease
         *
         * name++;
         *
         * ++name; for cross output
         */
        int myRoles = 50;

        myRoles--;
        System.out.println(myRoles);
    }
}
