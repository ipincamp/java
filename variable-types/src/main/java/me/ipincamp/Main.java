package me.ipincamp;

public class Main {
    public static void main(String [] args) {
        System.out.println("Variable Types\n" +
                "int, double, char, String, boolean\n");

        /**
         * Int
         */
        int ageInt = 21;
        System.out.println("Int > " + ageInt);

        /**
         * int age;
         * age = 21;
         *
         * System.out.println(age);
         */

        /**
         * Double
         */
        double ageDouble = 21.8;

        System.out.println("Double > " + ageDouble);

        /**
         * Boolean (true/false)
         */
        boolean isNight = true;

        System.out.println("Boolean > " + isNight);

        /**
         * Char
         */
        char letter = 'S'; // Can number, but only one quotes

        System.out.println("Char > " + letter);

        /**
         * String
         */
        String name = "ipincamp";

        System.out.println("String > " + name);

        /**
         * String name = null;
         *
         * System.out.println("String > " + name);
         */
    }
}
