package me.ipincamp;

public class Main {
    public static void main(String [] args) {
        int number = 20;
        number = number + 30;
        System.out.println("Sum: " + number);

        /**
         * + sum
         * - substraction
         * * multiplication
         * / distribution
         * % modulus
         */
        System.out.println(number / 8 + "." + number % 9);
    }
}
